def addition(num1, num2)
  sum = num1 + num2
  puts "#{num1} + #{num2}"
 return sum
end

def subtraction(num1, num2)
  result = num1 - num2
  puts "#{num1} - #{num2}"
 return result
end

def division(num1, num2)
  quotient = num1 / num2
  puts "#{num1} / #{num2}"
 return quotient
end

def multiplication(num1, num2)
  multiple_sum = num1 * num2
  puts "#{num1} * #{num2}"
 return multiple_sum
end

def modulo(num1, num2)
  remainder = num1 % num2
  puts "#{num1} % #{num2}"
 return remainder
end

def square_root(num)
  answer = Math.sqrt(num)
  puts answer
 return answer
end
