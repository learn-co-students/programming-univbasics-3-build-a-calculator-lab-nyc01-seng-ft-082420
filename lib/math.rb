end




def 
  
  subtraction(num1, num2)


  difference = (num1 - num2)
  
  difference
  
end




def 
  
  division(num1, num2)


  quotient = (num1/num2)
  
  quotient
  
end




def 
  
  multiplication(num1, num2)


  product = (num1 * num2)
  
  product
  
end




